package com.hxht.autodeploy.service.huigu;

class HandlerBean {
	private String code
	private String name
	private String status
	private String orderno

	String getCode() {
		return code
	}

	void setCode(String code) {
		this.code = code
	}

	String getName() {
		return name
	}

	void setName(String name) {
		this.name = name
	}

	String getStatus() {
		return status
	}

	void setStatus(String status) {
		this.status = status
	}

	String getOrderno() {
		return orderno
	}

	void setOrderno(String orderno) {
		this.orderno = orderno
	}

}
